namespace Itel.Framework.DAL.GraphQL.SA.Builders
{
    public enum ComparisonOperator
    {
        /// <summary>
        /// Great than '>'
        /// </summary>
        gt,
        /// <summary>
        /// Great than or equals '>='
        /// </summary>
        gte,
        /// <summary>
        /// Equals '=='
        /// </summary>
        eq,
        /// <summary>
        /// Not equals '!='
        /// </summary>
        neq,
        /// <summary>
        /// Less than '<'
        /// </summary>
        lt,
        /// <summary>
        /// Less than or equals '<='
        /// </summary>
        lte,
    }
}
